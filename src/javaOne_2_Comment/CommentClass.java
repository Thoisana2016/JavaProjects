package javaOne_2_Comment;

public class CommentClass {
    //SINGLE LINE COMMENT
    public void printSingle(){
        System.out.println("Hello ! How single line is comment writes? ");
        System.out.println("//This is how a comment is written!");
    }

    //MULTILINE COMMENT
    /*
        multiline comment is written this way
     */
    public void printMultiLine(){
        System.out.println("Hi! How multi line comment is written?");
        System.out.println("/*");
        System.out.println("this is how a multiline comment is written.");
        System.out.println("*/");
    }
}
